class SwapTwoNumber{

    public static void main(String[] args) {
        
        int a=10;
        int b=20;
        int c=0;


        System.out.println("Before Swapping a ="+a);
        System.out.println("Before Swapping b ="+b);
        c=a;
        a=b;
        b=c;

        System.out.println("after Swapping a ="+a);
        System.out.println("after Swapping a ="+b);
    }
}